// Day13: 高階関数 (map / filter / reduce)
//
// 高階関数は「関数を引数に取る関数」。
// コレクションの変換・絞り込み・集約をシンプルに書ける。
//
// map    : 各要素を変換して新しい配列を返す
//   [1, 2, 3].map { $0 * 2 }        // → [2, 4, 6]
//
// filter : 条件を満たす要素だけの配列を返す
//   [1, 2, 3, 4].filter { $0 % 2 == 0 }  // → [2, 4]
//
// reduce : 配列を1つの値に畳み込む
//   [1, 2, 3].reduce(0) { $0 + $1 }  // → 6

