// 【学習内容】
// Swift には変数(var)と定数(let)の2種類があります。
//
//   var name = "Taro"   // 変更できる
//   name = "Jiro"       // OK
//
//   let pi = 3.14       // 変更できない
//   pi = 3.0            // エラー!
//
// 【使い分けのルール】
// - 値が変わる可能性がある → var
// - 値が変わらない        → let (できるだけletを使う)
//
// =============================================================
// 【課題】以下の3つの関数を実装してください
// =============================================================

// 課題1:
// 名前を受け取って "Hello, {name}!" という文字列を返す関数
// 例: greet(name: "Swift") → "Hello, Swift!"
func greet(name: String) -> String {
    // ここに実装してください
    return "Hello, \(name)!"
}

// 課題2:
// 2つの整数を受け取って、合計を返す関数
// 例: add(a: 3, b: 5) → 8
func add(a: Int, b: Int) -> Int {
    // ここに実装してください
    return a + b
}

// 課題3:
// 整数を受け取って、それが偶数なら true、奇数なら false を返す関数
// ヒント: % は余り演算子です (4 % 2 == 0)
// 例: isEven(4) → true / isEven(7) → false
func isEven(_ number: Int) -> Bool {
    // ここに実装してください
    return (number % 2 == 0)
}
